    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try {

            if (rbtnNadador.isSelected()) {
                double auxMNadados = Double.parseDouble(txtMetrosNadados.getText());
                int auxnumCompetencias = Integer.parseInt(txtNumCompetencias.getText());
                if (auxnumCompetencias <= 0 || auxMNadados <= 0) {
                    JOptionPane.showMessageDialog(rootPane, "El valor no puede ser < o = a cero",
                            "ERROR", JOptionPane.ERROR_MESSAGE);
                } else {
                    depostistaGUI.add(new Nadador(txtNombre.getText(), Double.parseDouble(txtMetrosNadados.getText()), Integer.parseInt(txtNumCompetencias.getText())));
                }
            } else if (rbtnCiclista.isSelected()) {
                double auxKmRecorridos = Double.parseDouble(txtKmRecorridos.getText());
                int auxNumMarcas = Integer.parseInt(txtNumMarcas.getText());
                if (auxKmRecorridos <= 0 || auxNumMarcas <= 0) {
                    JOptionPane.showMessageDialog(rootPane, "El valor no puede ser < o = a cero",
                            "ERROR", JOptionPane.ERROR_MESSAGE);
                } else {
                    depostistaGUI.add(new Ciclista(txtNombre.getText(), Double.parseDouble(txtKmRecorridos.getText()), Integer.parseInt(txtNumMarcas.getText())));
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Se ha producido una excepción",
                    "Alerta!", JOptionPane.WARNING_MESSAGE);
        }

        limpiarInterfaz();
    }//GEN-LAST:event_jButton1ActionPerformed

