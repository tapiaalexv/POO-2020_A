    private void btnDeportistasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeportistasActionPerformed
        // TODO add your handling code here:
        for (Deportista d : depostistaGUI) {
            if (d instanceof Nadador) {
                ((Nadador) d).calcularBonoNadador();
                d.calcularGanancias();
                txaResumen.insert(d.toString(),0);
            } else if (d instanceof Ciclista) {
                ((Ciclista) d).calcularBonoCiclista();
                d.calcularGanancias();
                txaResumen.insert(d.toString(),0);
            }
        }

    }//GEN-LAST:event_btnDeportistasActionPerformed

