    private void txtBusquedaEdadKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaEdadKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            txtAreaResultados.setText("");
            
            int edadAux = Integer.parseInt(txtBusquedaEdad.getText());
            for(Perro p : perritos){
                if(p.getEdad() == edadAux){
                    txtAreaResultados.append(p.toString());
                }                    
            }
        }
    }//GEN-LAST:event_txtBusquedaEdadKeyPressed

    private void cmbBusquedaRazaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbBusquedaRazaActionPerformed
        // TODO add your handling code here:
        int indiceComboBox = cmbBusquedaRaza.getSelectedIndex();
        
        if(indiceComboBox  == 0){
            txtAreaResultados.setText("");
            for(Perro p : perritos){
                if(p.getRaza().equalsIgnoreCase("Bulldog")){
                    txtAreaResultados.append(p.toString());
                }                    
            }
            
        }else if(indiceComboBox == 1){
            txtAreaResultados.setText("");
            for(Perro p : perritos){
                if(p.getRaza().equalsIgnoreCase("Poodle")){
                    txtAreaResultados.append(p.toString());
                }                    
            }
        }else if(indiceComboBox == 2){
            txtAreaResultados.setText("");
            for(Perro p : perritos){
                if(p.getRaza().equalsIgnoreCase("Beagle")){
                    txtAreaResultados.append(p.toString());
                }                    
            }
        }else if(indiceComboBox == 3){
            txtAreaResultados.setText("");
            for(Perro p : perritos){
                if(p.getRaza().equalsIgnoreCase("Chihuahua")){
                    txtAreaResultados.append(p.toString());
                }                    
            }
        }
    }//GEN-LAST:event_cmbBusquedaRazaActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombreAux = txtNombre.getText();
        String generoAux = null;
        if(rdbtnMacho.isSelected()){
            generoAux=rdbtnMacho.getText();
        }
        if(rdbtnHembra.isSelected()){
            generoAux=rdbtnHembra.getText();
        }
        String razaAux = String.valueOf(cmbRaza.getSelectedItem());
        int edadAux = Integer.parseInt(txtEdad.getText());
        perritos[indice]=new Perro(nombreAux, generoAux, edadAux, razaAux);
        indice++;
        
    }//GEN-LAST:event_jButton1ActionPerformed

